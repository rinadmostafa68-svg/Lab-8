   public void loadLessons() {
    DefaultTableModel m = (DefaultTableModel) table.getModel();
    m.setRowCount(0);

    List<Lesson> list = cm.getLessonsForInstructor(courseId);
    

    for (Lesson l : list) {
        m.addRow(new Object[]{
            l.getLessonId(),
            l.getTitle(),
            l.getContent()
        });
    }
}


   
