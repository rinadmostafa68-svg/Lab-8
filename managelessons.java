    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
      int row = table.getSelectedRow();
if (row == -1) {
    JOptionPane.showMessageDialog(this, "Select a lesson first!");
    return;
}

String lessonId = table.getValueAt(row, 0).toString();

try {
    cm.deleteLesson(courseId, lessonId);
    JOptionPane.showMessageDialog(this, "Lesson deleted.");
    loadLessons(); 
} catch (Exception e) {
    JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
}       // TODO add your handling code here:
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
int row = table.getSelectedRow();
if (row == -1) {
    JOptionPane.showMessageDialog(this, "Select a lesson first!");
    return;
}

String lessonId = table.getValueAt(row, 0).toString();

String newTitle = JOptionPane.showInputDialog(this, "New title:");
if (newTitle == null) return;

String newContent = JOptionPane.showInputDialog(this, "New content:");
if (newContent == null) return;

try {
    cm.editLesson(courseId, lessonId, newTitle, newContent, new ArrayList<>());

    JOptionPane.showMessageDialog(this, "Lesson updated successfully!");
    loadLessons(); 
} catch (Exception e) {
    JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
}     // TODO add your handling code here:
    }//GEN-LAST:event_jButton3ActionPerformed

