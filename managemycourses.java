private void loadTable() {
    DefaultTableModel m = (DefaultTableModel) table.getModel();
    m.setRowCount(0);

    try {
        List<Course> list = cm.getInstructorCourses(instructor);

        for (Course c : list) {
            m.addRow(new Object[]{
                c.getCourseId(),
                c.getTitle(),
                c.getDescription()
            });
        }

    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
    }
}

   

