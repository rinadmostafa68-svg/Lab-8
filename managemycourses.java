    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
int row = table.getSelectedRow();
    if (row == -1) {
        JOptionPane.showMessageDialog(this, "Select a course first!");
        return;
    }

    String courseId = table.getValueAt(row, 0).toString();

    try {
        List<Student> list = cm.getEnrolledStudents(courseId);

        if (list.isEmpty()) {
            JOptionPane.showMessageDialog(this, "No students enrolled.");
            return;
        }

        StringBuilder sb = new StringBuilder();
        for (Student s : list){
            sb.append(s.getUsername())
              .append(" (").append(s.getEmail()).append(")\n");
        }

        JOptionPane.showMessageDialog(this, sb.toString());

    } catch (Exception e){
        JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
    }        // TODO add your handling code here:
    }//GEN-LAST:event_jButton5ActionPerformed

